'''
        Подвиг 4. На вход программы подается строка с номером телефона в формате: 

        +7(xxx)xxx-xx-xx
        
        Необходимо прочитать эту строку и преобразовать ее в список lst (посимвольно, то есть, 
        элементами списка будут являться отдельные символы строки). Затем, удалить первый '+', 
        число 7 заменить на 8 и убрать дефисы. Отобразить полученный список на экране командой:
        
        print("".join(lst))
        
        Sample Input:
        
        +7(912)123-45-67
        Sample Output:
        
        8(912)1234567
'''

lst = list(input())

lst.pop(0)
lst[0] = '8'
lst.remove("-")
lst.remove("-")
print("".join(lst))




'''
        Подвиг 5. На вход программы подается строка с именем, отчеством и фамилией, записанными через пробел. 
        Необходимо прочитать эту строку и представить прочитанные данные в виде новой строки в формате:

        Фамилия И.О.
        
        Например, строка: "Сергей Михайлович Балакирев" преобразуется в строку: "Балакирев С.М."
        
        
        Sample Input:
        
        Сергей Михайлович Балакирев
        Sample Output:
        
        Балакирев С.М.
'''

a, b, c = map(str, input().split())

b = list(b)[0] + "."
a = list(a)[0] + "."

print(f"{c} {a}{b}")


