n, m = 4, 5
# –°–æ–∑–¥–∞–µ–º –ø—É—Å—Ç—É—é –º–∞—Ç—Ä–∏—Ü—É n x m –∑–∞–ø–æ–ª–Ω–µ–Ω–Ω—É—é 0
matrix = [[0] * m for _ in range(n)]
# –ù–∞—á–∞–ª—å–Ω—ã–µ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã 0 0, –Ω–∞—á–∞–ª—å–Ω–æ–µ –¥–≤–∏–∂–µ–Ω–∏–µ –ø–æ –æ—Å—è–º y = 1 –ø–æ x = 0
dx, dy, x, y = 0, 1, 0, 0
for i in range(1, n * m + 1):
    matrix[x][y] = str(i).ljust(3)
    if matrix[(x + dx) % n][(y + dy) % m]:
        dx, dy = dy, -dx
    x += dx
    y += dy
# –≤—ã–≤–æ–¥–∏–º –º–∞—Ç—Ä–∏—Ü—É
[print(*row) for row in matrix]