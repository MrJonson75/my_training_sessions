def pascal(n: int) -> list:
    p = []
    for i in range(0, n+1):
        row = [1] * (i + 1)
        for j in range(i + 1):
            if j != 0 and j != i:
                row[j] = p[i - 1][j - 1] + p[i - 1][j]

        p.append(row)
    return p[-1]


